class dan0compression {
    Vector<datatable> data_tables;

    private int c_data[];
    private int globalposition;

    public dan0compression() {
        data_tables = new Vector<datatable>();
    }

    public void addtable(String name, int values[]) {
        datatable dt = new datatable(name);
        dt.copydata(values);
        data_tables.add(dt);
    }

    public void do_compression() {
        int j;
        int max = 0;
        for (j=0;j<data_tables.size();j++) {
            max += data_tables.elementAt(j).values.length;
        }
        c_data = new int[max];
        globalposition = compress();
    }

    private int compress() {
        int j;
        int position = 0;
        int index = 0;
        datatable tb = null;

        if (data_tables.size() == 0) return 0;

        if (data_tables.size() == 1) {
            index = 0;
            position = 1;
            c_data[0] = 0x81;
            tb = data_tables.elementAt(0);
        }

        if (data_tables.size() > 1) {

            int max = 0;
            int size;
            for (j=0;j<data_tables.size();j++) {
                max += data_tables.elementAt(j).values.length;
            }
            for (j=0;j<data_tables.size();j++) {
                if (data_tables.size() > 8) {
                    System.out.println (" ;; = " + j + " / " + data_tables.size());
                }
                tb = data_tables.elementAt(j);
                data_tables.removeElementAt(j);
                size = compress();
                if (size<max) {
                    max = size;
                    index = j;
                }
                data_tables.add(j,tb);
            }
            tb = data_tables.elementAt(index);
            data_tables.removeElementAt(index);
            position = compress();
            data_tables.add(index,tb);
        }
        // call compress

        position = compression(tb,position);

        return position;
    }

    private int compression(datatable tb, int position) {
        Vector<rlenode> rle = new Vector<rlenode>();
        rlenode details;
        int nbr;
        int values[];
        int raw[] = tb.values;
        Boolean rleflags[] = new Boolean[raw.length];
        Boolean rleflag;
        int i,j,k,l;

        // Analyze for RLE parts from RAW data
        rleflag = (Boolean) (raw[0]==raw[1]);
        for (i = 0; i < raw.length - 1; i++) {
            if (raw[i]==raw[i+1]) rleflag = true;
            rleflags[i] = rleflag;
            rleflag = (Boolean) (raw[i]==raw[i+1]);
        }
        rleflags[i] = rleflag;

        rleflag = true;
        // Avoid encoding isolated 2 bytes in a row
        while (rleflag == true) {
            rleflag = false;
            for (i=0; i<rleflags.length-3; i++) {
                if (rleflags[i] == false && rleflags[i+1] == true && rleflags[i+2] == true && rleflags[i+3] == false) {
                    rleflags[i+1] = false;
                    rleflags[i+2] = false;
                    rleflag = true;
                }
            }
            for (i=0; i<rleflags.length-3; i++) {
                if (rleflags[i] == false && rleflags[i+1] == true && rleflags[i+2] == true && rleflags[i+3] == true) {
                    if (raw[i+1] == raw[i+2] && raw[i+2] !=raw[i+3]) {
                        rleflags[i+1] = false;
                        rleflags[i+2] = false;
                        rleflag = true;
                    }
                }
            }
            for (i=0; i<rleflags.length-3; i++) {
                if (rleflags[i] == true && rleflags[i+1] == true && rleflags[i+2] == true && rleflags[i+3] == false) {
                    if (raw[i]!=raw[i+1] && raw[i+1]==raw[i+2]) {
                        rleflags[i+1] = false;
                        rleflags[i+2] = false;
                        rleflag = true;
                    }
                }
            }
        }

        // Encoding RLE
        i = 0;
        while (i<raw.length) {
            if (rleflags[i] == true) {
                k = raw[i];
                j = 0;
                while (i < raw.length && raw[i] == k) {
                    j++; i++;
                }
                values = new int[1];
                values[0] = k;
                details = new rlenode(true,j,values);
                rle.add(details);
                //System.out.println ("RLE : "+details.items[0] + ", nbr "+details.nbr_items); // + ", last "+details.items[details.items.length-1]);
            } else {
                j = 0;
                while ( j < raw.length-i && rleflags[i+j] == false) {
                    j++;
                }
                values = new int[j];
                for (k = 0; k < j; k++) {
                    values[k] = raw[i++];
                }
                details = new rlenode(false,j,values);
                rle.add(details);
               // System.out.println ("RAW : "+details.items[0] + ", nbr "+details.items.length + ", last "+details.items[details.items.length-1]);
            }
        }

        // Decompose big run lengths encoding into smaller ones
        i = 0;
        while (i<rle.size()) {
            details = rle.elementAt(i);
            nbr = details.nbr_items;
            if (nbr>127 && nbr!=256) {
                //System.out.println (details.flag + "nombre d'items : " + nbr + " @ " + i);
                rle.removeElementAt(i);
                if (details.flag) {
                    while (nbr >= 256) {
                        rlenode rlenode1 = new rlenode(true,256,details.items);
                        rle.add(i,rlenode1);
                        i++;
                        //System.out.println (" - 256");
                        nbr -= 256;
                    }
                    while (nbr >= 127 && nbr != 128 && nbr !=129) {
                        rlenode rlenode1 = new rlenode(true,127,details.items);
                        rle.add(i,rlenode1);
                        i++;
                        //System.out.println (" - 127");
                        nbr -= 127;
                    }
                    if (nbr == 128 || nbr == 129) {
                        rlenode rlenode1 = new rlenode(true,125,details.items);
                        rle.add(i,rlenode1);
                        i++;
                        //System.out.println (" - 125");
                        nbr -= 125;
                    }
                    if (nbr > 1) {
                        rlenode rlenode1 = new rlenode(true,nbr,details.items);
                        rle.add(i,rlenode1);
                        i++;
                        //System.out.println (" - "+nbr);
                        nbr = 0;
                    }
                    if (nbr == 1) {
                        rlenode rlenode1 = new rlenode(false,nbr,details.items);
                        rle.add(i,rlenode1);
                        i++;
                        //System.out.println (" - "+nbr);
                        nbr = 0;
                    }
                    i--;
                } else {
                    j = 0;
                    while (nbr >= 256) {
                        int items[] = new int[256];
                        System.arraycopy(details.items,j,items,0,256);
                        rlenode rlenode1 = new rlenode(false,256,items);
                        rle.add(i,rlenode1);
                        i++; j+=256;
                        //System.out.println (" - 256");
                        nbr -= 256;
                    }
                    if (nbr >= 127) {
                        int items[] = new int[127];
                        System.arraycopy(details.items,j,items,0,127);
                        rlenode rlenode1 = new rlenode(false,127,items);
                        rle.add(i,rlenode1);
                        i++; j+=127;
                        //System.out.println (" - 128");
                        nbr -= 127;
                    }
                    if (nbr > 0) {
                        int items[] = new int[nbr];
                        System.arraycopy(details.items,j,items,0,nbr);
                        rlenode rlenode1 = new rlenode(false,nbr,items);
                        rle.add(i,rlenode1);
                        i++; j+=nbr;
                        //System.out.println (" - "+nbr);
                        nbr -= nbr;
                    }
                }
            }
            i++;
        }

        rlehuffman rlecodes = tb.rlecodes;
        rlecodes.flush();
        tb.globalpointer = position;

        for (i = 0; i < rle.size(); i++) {
            details = rle.elementAt(i);
            rlecodes.addrlecode(details.flag,details.nbr_items);
            for (j = 0; j < details.items.length; j++) {
                Boolean ok = false;
                for (k = 1; k < 11; k++) { //  de -1 à -18 (>-19) ( = -(16+2+1))
                    if (position-k >= 0 && ok==false) {
                        if (details.items[j] == c_data[position-k]) {
                            ok = true;
                            rlecodes.addhuffmancode(k);
                        }
                    }
                }
                if (ok == false) {
                    rlecodes.addhuffmancode(0);
                    c_data[position++] = details.items[j];
                }
            }
        }
        rlecodes.addrlecode(true,0); // add END MARKER

        return position;
    }

    public void printout() {
        int i,j,k;
        datatable tb;
        int values[];
        int size;
        // HEADER
        System.out.println ("; /* ******************** *");
        System.out.println (";  * DAN0 COMPRESSED DATA *");
        System.out.println (";  * ******************** */");
        System.out.println ("");
        // GLOBAL DECLARATIONS
        System.out.println ("        .area _CODE");
        for (i = 0; i < data_tables.size() ; i++) {
            System.out.println ("        .globl _"+data_tables.elementAt(i).name);
        }
        System.out.println ("");
        // CONTROL TABLES
        for (i = 0; i < data_tables.size() ; i++) {
            tb = data_tables.elementAt(i);
            values = tb.rlecodes.values;
            size = tb.rlecodes.length;
            System.out.println ("_"+tb.name+":");
            System.out.println ("    .dw    dn0data + #"+(tb.globalpointer-1));
            int limit = 16;
            for (j = 0; j<size; j+=16) {
                String s = new String("    .db    #");
                if ((size-j)<limit) {limit = size-j;}
                for (k = 0; k < limit; k++) {
                    s = s.concat(String.valueOf(values[j+k]));
                    if (k != limit - 1) {
                        s = s.concat(",#");
                    }
                }
                System.out.println (s);
            }
        }
        // DATA TABLE
        System.out.println ("dn0data:");
        int limit = 16;
        for (j = 1; j < globalposition; j+=16) {
                String s = new String("    .db    #");
                if ((globalposition - j)<limit) { limit = globalposition - j; }
                for (k = 0; k < limit; k++) {
                    s = s.concat(String.valueOf(c_data[j+k]));
                    if (k != limit - 1) {
                        s = s.concat(",#");
                    }
                }
                System.out.println (s);
        }

    }

}

